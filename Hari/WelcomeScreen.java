    private void b_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_loginActionPerformed
        // TODO add your handling code here:
        try
        {
           String userid=tf_userid.getText();
            String password=new String(pf_password.getPassword());
            String usertype=cb_loginas.getSelectedItem().toString();
            PreparedStatement stmt = null;
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/?user=hari&password=ubuntupassword");
            if(usertype.equals("HS"))
            {
                  stmt=con.prepareStatement("SELECT * FROM `proj`.`STUDENTLIST` WHERE (`REG_NO`=? and `PASSWORD`=?) and `IS_MD`=?;");
                  stmt.setString(3,"TRUE");
                  
            }
              
            else
                if(usertype.equals("Student"))
                {
                    //System.out.println("Hai");
                    stmt=con.prepareStatement("SELECT * from proj.STUDENTLIST WHERE REG_NO=? AND PASSWORD=?;");

                }
                else
                    stmt=con.prepareStatement("SELECT * FROM proj.ADMIN WHERE REG_NO=? AND PASSWORD=?;");
            
            stmt.setString(1,userid);
            stmt.setString(2,password);
            ResultSet rs=stmt.executeQuery();
            if(rs.next()){
                tf_userid.setText("");
                pf_password.setText("");
                JOptionPane.showMessageDialog(null,"Login Success");
                if(usertype.equals("Admin"))
                {
                    
                    //new WardenDashboard();
                }
                else if((usertype.equals("Student")))
                        {
                            new StudentDashboard(userid,false);
                        }
                else
                {
                    new StudentDashboard(userid,true);
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Username or Password is incorrect","Invalid Credentials",JOptionPane.ERROR_MESSAGE);
                tf_userid.setText("");
                pf_password.setText("");
                
            }
            
        }
        catch(Exception e)
        {
          //JOptionPane.showMessageDialog(null, e);
            System.out.println(e);
        }
    }//GEN-LAST:event_b_loginActionPerformed

