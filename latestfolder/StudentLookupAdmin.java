    private void b_imagesearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_imagesearchActionPerformed
        try {
            // TODO add your handling code here:
            String studid=tf_studentimage.getText();
            con = new Connector().getCon();
            stmt=con.prepareStatement("SELECT STUDENTIMAGE FROM proj.STUDENTLIST WHERE REG_NO=?");
            stmt.setString(1,studid);
            ResultSet rs=stmt.executeQuery();
            if(rs.next()) {
                BufferedImage im = ImageIO.read(rs.getBinaryStream("STUDENTIMAGE"));
                Image newimg=im.getScaledInstance(250, 250, Image.SCALE_DEFAULT);
                
                l_studentimage.setIcon(new ImageIcon(newimg));
            }
            else {
                JOptionPane.showMessageDialog(null, "No such student");
            }
        } catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        } finally {
            try {
                  if(con!=null) con.close();
                  
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
            }
            try {
                  if(stmt!=null) stmt.close();
                  
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
            }
            
        }
    }//GEN-LAST:event_b_imagesearchActionPerformed

