    private void b_searchbynameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_searchbynameActionPerformed
        // TODO add your handling code here:
        try {
            String name=tf_namesearch.getText();
            con = new Connector().getCon();
            stmt=con.prepareStatement("select NAME, REG_NO, PHONE_NUMBER,EMAIL,ROOM_NO from proj.STUDENTLIST where NAME like '%"+ name + "%';");
            ResultSet rs= stmt.executeQuery();
            t_result.setModel(DbUtils.resultSetToTableModel(rs));
            stmt.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin.");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null,"Class Missing. Contact System Admin.");
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin.");
        } finally {
            try {
                if(con!=null) con.close();
            }
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin.");
            }
            
            try {
                if(stmt!=null) stmt.close();
            }
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin.");
            }
        }
    }//GEN-LAST:event_b_searchbynameActionPerformed

